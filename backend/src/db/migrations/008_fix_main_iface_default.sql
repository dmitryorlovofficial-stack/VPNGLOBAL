-- ============================================================
-- Миграция 008: Исправление дефолта main_iface
-- main_iface заполняется автоматически через scan агента,
-- дефолт 'eth0' давал неверные данные до первого сканирования
-- ============================================================

-- Убираем дефолт 'eth0', теперь NULL пока агент не просканирует
ALTER TABLE servers ALTER COLUMN main_iface SET DEFAULT NULL;

-- Обнуляем 'eth0' у серверов, где ещё не было сканирования
-- (os_info NULL = скан не проводился)
UPDATE servers SET main_iface = NULL WHERE main_iface = 'eth0' AND os_info IS NULL;
